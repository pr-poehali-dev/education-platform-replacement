4.2. При возникновении пожара в электроустановке:
- сообщить в пожарную охрану по телефону 101 или 112;
- сообщить диспетчеру и руководству;
- по возможности обесточить электроустановку;
- приступить к тушению пожара углекислотными или порошковыми огнетушителями (не применять воду или пенные огнетушители на оборудовании, находящемся под напряжением);
