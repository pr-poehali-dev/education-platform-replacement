## III. ТРЕБОВАНИЯ ОХРАНЫ ТРУДА ВО ВРЕМЯ РАБОТЫ

3.1. **Работы в электроустановках до 1000 В:**
- работы со снятием напряжения выполняются по наряду-допуску или распоряжению;
- работы без снятия напряжения (на расстоянии) выполняются не менее чем двумя работниками с применением изолирующих средств защиты;
- работы без снятия напряжения (вблизи или прикосновением к токоведущим частям) выполняются по наряду-допуску не менее чем двумя работниками с применением диэлектрических перчаток, инструмента с изолированными рукоятками, диэлектрических ковриков или галош.

3.2. **Работы в электроустановках выше 1000 В:**
- все работы выполняются только по наряду-допуску;
- работы выполняются бригадой не менее двух человек (производитель работ и член бригады);
- производитель работ должен иметь группу IV (в электроустановках до 35 кВ) или V (выше 35 кВ);
- члены бригады должны иметь группу III.

3.3. Правила применения средств защиты:
- перед применением средства защиты необходимо осмотреть на предмет отсутствия внешних повреждений;
- проверить наличие штампа о сроке проведения испытаний;
- диэлектрические перчатки перед применением проверить на отсутствие проколов (растянуть в сторону пальцев);
- при работе под напряжением до 1000 В применять диэлектрические перчатки в сочетании с инструментом с изолированными рукоятками;
- при работе в электроустановках выше 1000 В применять изолирующие штанги или клещи в сочетании с диэлектрическими перчатками;
- работать стоя на диэлектрическом коврике или в диэлектрических ботах (галошах).

3.4. Проверка отсутствия напряжения:
- проверять указателем напряжения, предназначенным для данного класса напряжения;
- перед проверкой убедиться в исправности указателя напряжения на токоведущих частях, заведомо находящихся под напряжением;
- проверять отсутствие напряжения на всех фазах (полюсах);
- после проверки отсутствия напряжения установить переносное заземление.

3.5. Установка переносного заземления:
- переносное заземление устанавливается на токоведущие части после проверки отсутствия напряжения;
- сначала переносное заземление присоединяется к заземляющему устройству, затем (после проверки отсутствия напряжения) накладывается на токоведущие части;
- установка переносного заземления в электроустановках выше 1000 В производится изолирующей штангой в диэлектрических перчатках;
- снятие переносного заземления производится в обратной последовательности.

3.6. Правила безопасности при работе:
- работать только на участке, огражденном и обозначенном плакатами;
- не заходить за ограждения, не прикасаться к оставшимся под напряжением токоведущим частям;
- применять инструмент и приспособления по назначению, содержать их в исправном состоянии;
- следить за исправностью средств защиты;
- соблюдать расстояния до токоведущих частей, находящихся под напряжением;
- выполнять требования производителя работ (наблюдающего).

3.7. ЗАПРЕЩАЕТСЯ:
- работать в электроустановках без соответствующей группы по электробезопасности;
- работать без применения средств защиты;
- работать неисправным инструментом;
- прикасаться к токоведущим частям, находящимся под напряжением, без средств защиты;
- работать при недостаточной освещенности;
- работать в одиночку в электроустановках выше 1000 В;
- заходить в помещения с высоковольтным оборудованием без разрешения ответственного лица;
- оставлять рабочее место без разрешения производителя работ (наблюдающего);
- прикасаться к изоляторам под напряжением.

