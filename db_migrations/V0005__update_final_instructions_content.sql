## I. ОБЩИЕ ПОЛОЖЕНИЯ

1.1. Настоящая должностная инструкция определяет и регламентирует функциональные обязанности, права и ответственность мастера производственного участка (далее - Мастер).

1.2. Мастер относится к категории руководителей низшего звена, назначается на должность и освобождается от должности приказом генерального директора организации по представлению начальника цеха.

1.3. Мастер подчиняется непосредственно начальнику производственного цеха (участка) или его заместителю.

1.4. На должность мастера производственного участка назначается лицо, имеющее:
- среднее профессиональное образование (программа подготовки специалистов среднего звена) и стаж работы на производстве не менее 2 лет;
- или высшее образование (бакалавриат) без предъявления требований к стажу работы;
- или высшее образование (специалитет, магистратура).

1.5. Мастер должен знать:
- законодательные и нормативные правовые акты, регламентирующие производственно-хозяйственную и финансово-экономическую деятельность организации;
- технологию производства продукции участка;
- технические требования, предъявляемые к продукции;
- технологическое оборудование участка, принцип его работы;
- систему планово-предупредительного ремонта оборудования;
- основы организации труда и управления;
- основы экономики и организации производства;
- трудовое законодательство Российской Федерации;
- правила внутреннего трудового распорядка;
- правила и нормы охраны труда, техники безопасности, производственной санитарии и противопожарной защиты.

1.6. В период временного отсутствия мастера его обязанности возлагаются на заместителя или старшего рабочего участка.

## II. ФУНКЦИОНАЛЬНЫЕ ОБЯЗАННОСТИ

Мастер выполняет следующие должностные обязанности:

2.1. **Организация производственного процесса:**
- организует работу производственного участка в соответствии с производственным планом;
- обеспечивает своевременное получение оборудования, инструментов, материалов, необходимых для работы;
- распределяет производственные задания среди работников участка;
- координирует работу работников на участке;
- контролирует соблюдение технологических процессов;
- контролирует рациональное использование материальных ресурсов;
- обеспечивает выполнение производственных заданий участком в установленные сроки.

2.2. **Контроль качества продукции:**
- осуществляет контроль качества выпускаемой продукции;
- выявляет причины брака и принимает меры по его предупреждению и устранению;
- участвует в разработке мероприятий по повышению качества продукции;
- обеспечивает соблюдение технологических режимов;
- контролирует соблюдение работниками технических требований и стандартов.

2.3. **Управление персоналом:**
- руководит работой рабочих участка;
- проводит инструктажи по охране труда на рабочем месте;
- организует обучение работников безопасным методам работы;
- контролирует соблюдение работниками трудовой и производственной дисциплины;
- ведет учет рабочего времени;
- оформляет листы временной нетрудоспособности;
- участвует в подборе и расстановке кадров на участке;
- организует проведение стажировки новых работников.

2.4. **Обеспечение безопасности труда:**
- обеспечивает соблюдение работниками требований охраны труда и промышленной безопасности;
- контролирует использование работниками средств индивидуальной защиты;
- организует проведение инструктажей по охране труда;
- осуществляет контроль за состоянием охраны труда на участке;
- участвует в расследовании несчастных случаев и инцидентов на участке;
- принимает меры по улучшению условий труда;
- организует проведение обязательных медицинских осмотров работников.

2.5. **Техническое обслуживание оборудования:**
- контролирует техническое состояние оборудования;
- организует своевременное проведение технического обслуживания и ремонта;
- выявляет неисправности оборудования и принимает меры по их устранению;
- участвует в приемке оборудования из ремонта;
