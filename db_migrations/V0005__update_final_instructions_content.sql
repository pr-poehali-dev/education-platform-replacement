  * в электроустановках выше 1000 В - только после снятия напряжения или наложения переносного заземления;
- вызвать скорую медицинскую помощь по телефону 103 или 112;
- оказать первую помощь:
  * при отсутствии сознания и дыхания - провести сердечно-легочную реанимацию;
  * при наличии сознания - уложить, обеспечить покой, расстегнуть одежду;
  * при ожогах - наложить стерильную повязку;
