## I. ОБЩИЕ ТРЕБОВАНИЯ ОХРАНЫ ТРУДА

1.1. Настоящая инструкция разработана на основе Правил по охране труда при выполнении работ на объектах связи, утвержденных Приказом Минтруда России от 07.12.2020 № 867н, и Правил противопожарного режима в Российской Федерации, утвержденных Постановлением Правительства РФ от 16.09.2020 № 1479.

1.2. К выполнению сварочных работ допускаются работники:
- не моложе 18 лет;
- прошедшие обязательный предварительный и периодические медицинские осмотры;
- прошедшие обучение по профессии сварщика;
- имеющие удостоверение сварщика установленного образца;
- прошедшие обучение и проверку знаний требований охраны труда;
- прошедшие обучение пожарно-техническому минимуму;
- имеющие группу по электробезопасности не ниже II (при работе с электросварочным оборудованием).

1.3. Сварщик должен знать:
- устройство и принцип работы сварочного оборудования;
- правила безопасной эксплуатации газовых баллонов;
- способы и приемы безопасного выполнения сварочных работ;
- правила применения средств индивидуальной защиты;
- правила пожарной безопасности при проведении сварочных работ;
- способы оказания первой помощи при несчастных случаях.

1.4. Опасные и вредные производственные факторы при сварочных работах:
- повышенное значение напряжения в электрической цепи (при электросварке);
- повышенная температура свариваемых изделий, оборудования и материалов;
- повышенная яркость света (ультрафиолетовое и инфракрасное излучение дуги);
- повышенное значение напряженности электрического и магнитного полей;
- повышенная загазованность воздуха рабочей зоны (сварочные аэрозоли, оксиды азота, озон, угарный газ);
- разбрызгивание расплавленного металла и шлака;
- взрыво- и пожароопасность (при газовой сварке);
- повышенный уровень шума;
- повышенная или пониженная температура воздуха рабочей зоны;
- острые кромки, заусенцы на поверхностях заготовок и оборудования.

1.5. При выполнении сварочных работ должны применяться следующие средства индивидуальной защиты:
- костюм сварщика из огнестойких материалов;
- ботинки кожаные на резиновой подошве;
- краги сварщика;
- щиток сварщика или маска сварщика со светофильтром;
- очки защитные открытые (для вспомогательных работ);
- респиратор противоаэрозольный (при работе в плохо проветриваемых помещениях);
- перчатки диэлектрические (при электросварочных работах);
- наушники или вкладыши противошумные (при повышенном уровне шума);
- каска защитная (при работе в производственных помещениях и на строительных площадках).

## II. ТРЕБОВАНИЯ ОХРАНЫ ТРУДА ПЕРЕД НАЧАЛОМ РАБОТЫ

2.1. Надеть исправную спецодежду, спецобувь и средства индивидуальной защиты:
- костюм сварщика должен быть застегнут, не иметь масляных пятен;
- брюки должны быть надеты навыпуск поверх обуви;
- рукава куртки должны быть застегнуты на пуговицы или опущены на краги;
- карманы должны быть закрыты клапанами.

2.2. Получить задание на выполнение работы:
- при огневых работах - получить наряд-допуск на выполнение огневых работ;
- ознакомиться с содержанием наряда-допуска и расписаться в журнале;
- убедиться в выполнении всех мероприятий, указанных в наряде-допуске.

2.3. Подготовить рабочее место:
- удалить из зоны сварки легковоспламеняющиеся и горючие материалы на расстояние не менее 5 м (при невозможности - укрыть несгораемыми материалами);
- установить ограждения и вывесить предупреждающие знаки;
- подготовить средства пожаротушения (огнетушитель, ящик с песком, ведро с водой);
- обеспечить естественную или принудительную вентиляцию;
- проверить освещенность рабочего места (не менее 200 лк).

2.4. Проверить исправность сварочного оборудования:
- электросварочный аппарат должен быть заземлен;
- изоляция сварочных проводов должна быть без повреждений;
- электрододержатель должен быть исправен, обеспечивать надежное крепление электрода;
- для газовой сварки: проверить исправность редукторов, шлангов, горелки;
- для газовой сварки: проверить плотность соединений (обмыливанием), отсутствие утечек газа.

2.5. При работе с газовыми баллонами убедиться в:
- наличии на баллонах окраски и надписей, соответствующих газу;
- наличии предохранительных колпаков на вентилях;
- отсутствии повреждений корпуса баллона;
- исправности редукторов и манометров;
- расстоянии между баллоном и источником открытого огня не менее 5 м;
- вертикальном положении баллонов и надежном их закреплении.

2.6. Проверить наличие и исправность защитного щитка или маски со светофильтром.

2.7. При обнаружении неисправностей сообщить непосредственному руководителю и до их устранения к работе не приступать.

## III. ТРЕБОВАНИЯ ОХРАНЫ ТРУДА ВО ВРЕМЯ РАБОТЫ

3.1. Общие требования при выполнении сварочных работ:
- работать только на исправном оборудовании;
- применять средства индивидуальной защиты;
- не допускать присутствия посторонних лиц в зоне сварки;
- обеспечивать свободный доступ к средствам пожаротушения;
- следить за исправностью вентиляции;
- не загромождать проходы и подходы к оборудованию.

3.2. При электродуговой сварке:
- не работать на открытом воздухе при дожде и снегопаде без навеса;
- перед зажиганием дуги убедиться, что вблизи нет людей без защитных средств;
- электрододержатель брать только в сухих рукавицах или кр агах;
- не допускать соприкосновения электрода или электрододержателя с телом или мокрой одеждой;
- не перегружать сварочный аппарат;
- при смене электрода отключать сварочный аппарат от сети;
- при перерывах в работе отключать сварочный аппарат и подвешивать электрододержатель на специальный крюк;
- не оставлять электрододержатель на свариваемом изделии.

3.3. При газовой сварке и резке:
- не работать от одного источника питания газом более двух сварщиков;
- перед зажиганием горелки проверить отсутствие утечек газа;
- зажигать горелку специальным зажигательным приспособлением или спичкой (не от постороннего пламени);
- открывать вентили на газовых баллонах плавно, без рывков;
- не допускать обратного удара пламени (при появлении хлопков немедленно перекрыть подачу газа);
- не перегревать горелку (при нагреве - охладить водой);
- не класть горелку и резак на свариваемое изделие или металл;
- при длительных перерывах (обед, конец смены) закрывать вентили баллонов и редукторов;
- при обнаружении утечки газа немедленно прекратить работу, перекрыть вентили.

3.4. При работе в закрытых емкостях, колодцах, тоннелях:
- работать не менее чем двум работникам (один внутри, второй снаружи);
- применять принудительную вентиляцию;
- применять переносное освещение напряжением не выше 12 В;
- каждые 10-15 минут делать перерывы для выхода на свежий воздух;
- работать под наблюдением ответственного лица.

3.5. При работе на высоте:
- применять страховочную систему (предохранительный пояс со страховочным канатом);
- использовать леса, подмости или люльки;
- следить за тем, чтобы внизу не находились люди и горючие материалы.

3.6. ЗАПРЕЩАЕТСЯ:
- выполнять сварку на свежеокрашенных горючими красками конструкциях и изделиях;
- производить сварку сосудов и трубопроводов, находящихся под давлением или нагрузкой;
- пр оизводить сварку аппаратов, заполненных горючими веществами;
- использовать провода и кабели с поврежденной изоляцией;
- охлаждать горелку или резак путем погружения в воду;
- работать неисправным инструментом;
- смазывать редукторы, вентили и другие части кислородной аппаратуры маслом или жиром;
- допускать попадание на кислородный редуктор масла, жира или ветоши;
- работать без применения средств индивидуальной защиты;
- присутствовать в зоне сварки без защитных щитков или очков.

## IV. ТРЕБОВАНИЯ ОХРАНЫ ТРУДА В АВАРИЙНЫХ СИТУАЦИЯХ

4.1. При возгорании:
- прекратить сварочные работы;
- отключить сварочное оборудование;
- закрыть вентили газовых баллонов;
- приступить к тушению пожара имеющимися средствами пожаротушения;
- вызвать пожарную охрану по телефону 101 или 112;
- сообщить о пожаре непосредственному руководителю;
- при невозможности ликвидации пожара - эвакуироваться в безопасное место.

4.2. При обратном ударе пламени (хлопке в горелке):
- немедленно перекрыть вентиль подачи кислорода, затем ацетилена;
- охладить горелку в воде;
- установить причину обратного удара;
- проверить исправность горелки;
- только после устранения причин возобновить работу.

4.3. При поражении электрическим током:
- отключить сварочный аппарат от электросети;
- освободить пострадавшего от действия электрического тока (оттянуть изолирующими средствами);
- вызвать скорую медицинскую помощь по телефону 103 или 112;
- оказать первую помощь:
  * при отсутствии сознания и дыхания - провести сердечно-легочную реанимацию;
  * при наличии сознания - уложить, обеспечить покой;
  * при ожогах - наложить стерильную повязку;
- сообщить о несчастном случае руководству.

4.4. При отравлении сварочными газами:
- вывести (вынести) пострадавшего на свежий воздух;
- расстегнуть одежду, стесняющую дыхание;
- вызвать скорую медицинскую помощь;
- при отсутствии дыхания провести искусственное дыхание;
- обеспечить покой до прибытия медработников.

4.5. При ожогах:
- прекратить действие поражающего фактора;
- охладить обожженную поверхность холодной водой (10-15 минут);
- наложить стерильную повязку;
- при обширных ожогах укрыть пострадавшего чистой тканью;
- вызвать скорую медицинскую помощь;
- не смазывать ожог маслом, жиром, не вскрывать пузыри.

## V. ТРЕБОВАНИЯ ОХРАНЫ ТРУДА ПО ОКОНЧАНИИ РАБОТЫ

5.1. Отключить сварочное оборудование от источников питания.

5.2. При газовой сварке:
- закрыть вентили на газовых баллонах;
- закрыть редукторы;
- выпустить газ из шлангов;
- повесить горелку на специальный крюк.

5.3. Убрать электроды, электрододержатель, провода и другие приспособления в отведенное для хранения место.

5.4. Осмотреть место проведения сварочных работ:
- убедиться в отсутствии очагов горения;
- проверить отсутствие тлеющих материалов;
- провести влажную уборку рабочего места;
- при работе по наряду-допуску - выполнить послесварочную выдержку (время, указанное в наряде).

5.5. Сдать наряд-допуск лицу, его выдавшему (если работа проводилась по наряду).

5.6. Снять спецодежду и средства индивидуальной защиты, осмотреть на предмет повреждений и прожогов.

5.7. Вымыть руки и лицо теплой водой с мылом или принять душ.

5.8. Сообщить непосредственному руководителю:
- о выполненной работе;
- обо всех неисправностях оборудования, обнаруженных во время работы;
- о всех нарушениях требований охраны труда и пожарной безопасности.

