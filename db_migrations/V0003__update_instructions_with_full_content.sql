## I. ОБЩИЕ ТРЕБОВАНИЯ ОХРАНЫ ТРУДА

1.1. Настоящая инструкция разработана на основе Правил по охране труда при эксплуатации электроустановок, утвержденных Приказом Минтруда России от 15.12.2020 № 903н.

1.2. К самостоятельной работе в качестве электромонтера допускаются лица:
- не моложе 18 лет;
- прошедшие обучение и проверку знаний по электробезопасности;
- имеющие группу по электробезопасности не ниже III (до 1000 В);
