1.3. К выполнению работ на высоте допускаются работники:
- не моложе 18 лет;
- прошедшие обязательные предварительные и периодические медицинские осмотры;
- прошедшие обучение безопасным методам и приемам выполнения работ на высоте;
- имеющие соответствующую группу по безопасности работ на высоте (1, 2 или 3);
- получившие допуск после проверки знаний требований охраны труда.

## II. ГРУППЫ БЕЗОПАСНОСТИ РАБОТ НА ВЫСОТЕ

2.1. **1-я группа** - работники, допускаемые к работам в составе бригады под руководством ответственного лица.
- Обучение и проверка знаний проводятся не реже одного раза в **3 года**.

2.2. **2-я группа** - мастера, бригадиры, руководители стажировки, ответственные исполнители работ по наряду-допуску.
- Обучение и проверка знаний проводятся не реже одного раза в **3 года**.

2.3. **3-я группа** - работники, ответственные за организацию и безопасное проведение работ, составление плана производства работ.
