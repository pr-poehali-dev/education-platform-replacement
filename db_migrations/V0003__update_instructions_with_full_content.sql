## III. ТРЕБОВАНИЯ ОХРАНЫ ТРУДА ПЕРЕД НАЧАЛОМ РАБОТЫ

3.1. Получить задание на выполнение работы от ответственного лица.

3.2. Пройти инструктаж по охране труда на рабочем месте.

3.3. Проверить наличие и исправность:
- средств индивидуальной защиты от падения с высоты (страховочная привязь, стропы, канаты, карабины);
- касок защитных;
- спецодежды и спецобуви;
- средств коллективной защиты (ограждения, защитные сетки, знаки безопасности).

3.4. Перед началом работы по наряду-допуску:
- проверить соответствие условий работы тем, которые указаны в наряде-допуске;
- убедиться в наличии плана эвакуации и спасения работников при возникновении аварийной ситуации;
- установить ограждения опасных зон и вывесить предупреждающие знаки.

3.5. Убедиться в достаточной освещенности рабочего места (не менее 50 лк при работе на открытой территории, не менее 200 лк в помещениях).

## IV. ТРЕБОВАНИЯ ОХРАНЫ ТРУДА ВО ВРЕМЯ РАБОТЫ

4.1. Использовать средства индивидуальной защиты от падения с высоты:
- страховочная привязь должна быть правильно надета и отрегулирована;
- стропы должны быть присоединены к анкерным устройствам;
- не допускается работа без закрепления к анкерной линии.

4.2. Работать только в пределах территории, указанной в наряде-допуске или производственной инструкции.

4.3. При работе на лестницах и стремянках:
- угол наклона приставных лестниц к горизонтали должен составлять не более 75°;
- длина приставной лестницы не должна превышать 5 м;
- лестница должна быть укреплена от сдвига и опрокидывания;
- запрещается работать на верхних трех ступенях приставной лестницы, не имеющей перил.

4.4. При работе с применением лесов и подмостей:
- леса и подмости должны быть испытаны и приняты по акту;
- настилы лесов должны быть сплошными, без щелей и зазоров;
- леса должны иметь бортовое ограждение высотой не менее 1,1 м.

4.5. При работе с использованием систем канатного доступа (промышленный альпинизм):
- использовать не менее двух независимых страховочных систем;
- перед началом спуска или подъема проверить надежность закрепления канатов.

4.6. При неблагоприятных погодных условиях работы на высоте на открытом воздухе должны быть прекращены:
- при скорости ветра 15 м/с и более;
- при гололеде, грозе, тумане (видимость менее 50 м);
- при температуре воздуха ниже -40°C или выше +40°C (при отсутствии защитных мероприятий).

4.7. Не допускается:
- работать без применения средств защиты от падения;
- находиться под местом проведения работ на высоте;
- сбрасывать с высоты материалы, инструмент и мусор без принятия мер безопасности;
- перемещаться по конструкциям без использования средств подмащивания.

## V. ТРЕБОВАНИЯ ОХРАНЫ ТРУДА В АВАРИЙНЫХ СИТУАЦИЯХ

5.1. При возникновении аварийной ситуации (падение работника, разрушение конструкций, возгорание):
- немедленно прекратить работы;
- сообщить ответственному лицу и диспетчерской службе;
- приступить к эвакуации людей в соответствии с планом эвакуации.

5.2. При падении работника с высоты:
- вызвать скорую медицинскую помощь;
- приступить к спасению пострадавшего с использованием средств спасения;
- оказать первую помощь до прибытия медицинских работников;
- сохранить место происшествия до расследования несчастного случая.

5.3. При обрыве страховочного каната:
- зафиксировать положение тела;
- использовать резервную страховочную систему;
- вызвать помощь.

## VI. ТРЕБОВАНИЯ ОХРАНЫ ТРУДА ПО ОКОНЧАНИИ РАБОТЫ

