- прошедшие вводный и первичный инструктаж на рабочем месте.

1.3. Группы по электробезопасности:
- **II группа** - персонал, обслуживающий электроустановки до 1000 В без права единоличного осмотра;
- **III группа** - электротехнический персонал, имеющий право единоличного осмотра электроустановок до 1000 В;
- **IV группа** - ответственные за электрохозяйство, имеющие право проведения работ и руководства бригадой.

1.4. Периодичность проверки знаний:
- для электротехнического персонала - не реже 1 раза в год;
- для административно-технического персонала - не реже 1 раза в 3 года.

1.5. Опасные производственные факторы при работе в электроустановках:
- повышенное значение напряжения электрической цепи;
- электрическая дуга и ее излучение;
- повышенная или пониженная температура воздуха рабочей зоны;
- повышенный уровень шума и вибрации;
- недостаточная освещенность рабочей зоны;
- острые кромки, заусенцы на поверхностях оборудования.

## II. ТРЕБОВАНИЯ ОХРАНЫ ТРУДА ПЕРЕД НАЧАЛОМ РАБОТЫ

2.1. Надеть исправную спецодежду, спецобувь и средства индивидуальной защиты:
- костюм хлопчатобумажный или из смесовых тканей;
- ботинки кожаные на резиновой подошве;
- каску защитную (при работе в производственных помещениях);
- перчатки диэлектрические (при работе под напряжением);
- очки защитные (при работах с риском попадания искр).

2.2. Получить задание на работу:
- при работе по наряду-допуску - ознакомиться с содержанием наряда, расписаться в журнале;
- при работе по распоряжению - получить указания от ответственного лица;
- при работе в порядке текущей эксплуатации - определить объем и характер работ.

2.3. Осмотреть рабочее место и убедиться в:
- достаточной освещенности;
- отсутствии посторонних предметов;
- наличии и исправности инструмента, приспособлений, средств защиты.

2.4. Проверить исправность:
- электроизмерительных приборов;
- диэлектрических средств защиты (перчаток, ковриков, галош, изолирующих штанг);
- ручного электроинструмента;
- переносных заземлений;
- предупреждающих плакатов и знаков безопасности.

2.5. Перед началом работы в электроустановках должны быть выполнены технические мероприятия:
- произведены необходимые отключения и приняты меры, препятствующие подаче напряжения на место работы;
- на приводах ручного и на ключах дистанционного управления вывешены запрещающие плакаты;
- проверено отсутствие напряжения на токоведущих частях;
- наложено заземление;
- вывешены плакаты, ограждены при необходимости рабочие места и оставшиеся под напряжением токоведущие части.

## III. ТРЕБОВАНИЯ ОХРАНЫ ТРУДА ВО ВРЕМЯ РАБОТЫ

3.1. Работы в электроустановках выполнять с соблюдением следующих условий:
- единоличный осмотр электроустановок до 1000 В разрешается работнику с группой III;
- в электроустановках до 1000 В работы должны выполняться не менее чем двумя лицами (допускающий и производитель работ);
- при работах со снятием напряжения применять переносное заземление.

3.2. При работе с электроинструментом:
- перед включением проверить целостность изоляции кабеля и вилки;
- работать в диэлектрических перчатках;
- не держать инструмент за токоведущий кабель;
- не работать в сырых помещениях без применения электроинструмента класса II или III;
- отключать инструмент от сети при перерывах в работе, регулировке, смене оснастки.

3.3. При работе под напряжением до 1000 В:
- применять диэлектрические перчатки в сочетании с изолирующими штангами или клещами;
- пользоваться инструментом с изолированными рукоятками;
- стоять на диэлектрическом коврике или в диэлектрических галошах;
- вблизи токоведущих частей, находящихся под напряжением, работать в одежде с застегнутыми рукавами.

3.4. Перед применением диэлектрических средств защиты убедиться в:
- их исправности и отсутствии внешних повреждений;
- соответствии напряжению электроустановки;
- наличии штампа о проведенных эксплуатационных испытаниях.

3.5. При обнаружении неисправности электрооборудования:
- немедленно отключить оборудование от сети;
- оградить опасную зону;
- сообщить непосредственному руководителю;
- не производить ремонт самостоятельно без разрешения.

3.6. Запрещается:
- работать в электроустановках в состоянии алкогольного или наркотического опьянения;
- касаться токоведущих частей, находящихся под напряжением, без применения средств защиты;
- работать при недостаточной освещенности;
- применять неисправный инструмент или средства защиты;
- допускать к работе посторонних лиц;
- хранить на рабочем месте легковоспламеняющиеся материалы.

## IV. ТРЕБОВАНИЯ ОХРАНЫ ТРУДА В АВАРИЙНЫХ СИТУАЦИЯХ

4.1. При возникновении пожара в электроустановке:
- обесточить оборудование;
- приступить к тушению огня с применением углекислотных или порошковых огнетушителей (не применять воду!);
- вызвать пожарную охрану по телефону 101;
- сообщить руководству организации.

4.2. При поражении электрическим током:
- освободить пострадавшего от действия тока (отключить электроустановку или оттянуть пострадавшего с помощью изолирующих средств);
- вызвать скорую медицинскую помощь по телефону 103 или 112;
- приступить к оказанию первой помощи:
  * при отсутствии сознания и дыхания - провести сердечно-легочную реанимацию;
  * при наличии сознания - уложить пострадавшего, обеспечить покой;
  * при ожогах - наложить стерильную повязку.

4.3. При обнаружении запаха гари, дыма, искрения:
- немедленно отключить электроустановку;
- сообщить дежурному персоналу или непосредственному руководителю;
- до выяснения причин к работе не приступать.

## V. ТРЕБОВАНИЯ ОХРАНЫ ТРУДА ПО ОКОНЧАНИИ РАБОТЫ

5.1. Отключить электрооборудование и инструмент от электрической сети.

5.2. Убрать инструмент и приспособления в отведенное место.

5.3. Снять переносное заземление (если оно было установлено).

5.4. Привести в порядок рабочее место.

5.5. Снять спецодежду и средства индивидуальной защиты, поместить в шкаф.

5.6. При работе по наряду-допуску:
- сообщить производителю работ (допускающему) об окончании работ;
- расписаться в наряде-допуске о закрытии наряда.

5.7. Доложить руководителю:
- о выполненной работе;
- обо всех неисправностях и нарушениях, обнаруженных во время работы;
- о принятых мерах по их устранению.

