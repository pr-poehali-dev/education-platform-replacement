## I. ОБЩИЕ ТРЕБОВАНИЯ ОХРАНЫ ТРУДА

1.1. Настоящая инструкция разработана на основе Правил по охране труда при работе на высоте, утвержденных Приказом Минтруда России от 16.11.2020 № 782н (действуют с 01.01.2021).

1.2. К работам на высоте относятся работы, при которых:
- существуют риски падения работника с высоты 1,8 м и более;
- работник осуществляет подъем на высоту более 5 м по вертикальной лестнице с углом наклона более 75°;
- работы производятся на площадках на расстоянии ближе 2 м от неогражденных перепадов по высоте более 1,8 м;
