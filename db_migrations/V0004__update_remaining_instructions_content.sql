3.6. ЗАПРЕЩАЕТСЯ:
- поднимать грузы массой, превышающей грузоподъемность крана;
- поднимать груз, находящийся в неустойчивом положении;
- поднимать груз, засыпанный землей, примерзший к земле, заложенный другими предметами;
- поднимать груз при нахождении людей на нем или под ним;
- перемещать груз над людьми;
- оставлять кран с грузом на крюке без надзора;
- работать при неисправных приборах и устройствах безопасности;
- работать при скорости ветра, превышающей допустимую (указывается в паспорте крана);
- работать при недостаточной видимости (менее 5 м);
- поднимать груз, масса которого не известна;
- вытаскивать краном застрявшие в штабеле грузы;
- подтаскивать груз по земле при косом натяжении каната;
- работать с неисправными грузозахватными приспособлениями.

