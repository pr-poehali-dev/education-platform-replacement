- не превышать грузоподъемность крана;
- поднимать груз плавно, без рывков и резких торможений;
- не оставлять груз в подвешенном состоянии при окончании работы или перерыве;
- следить за тем, чтобы люди не находились под поднимаемым грузом.

3.2. Правила строповки грузов:
- строповку груза производить только стропальщиком, имеющим соответствующее удостоверение;
- применять способ строповки в соответствии со схемами строповки;
- центр тяжести груза должен находиться на вертикали, проходящей через ось крюка;
- угол между ветвями стропа не должен превышать 90°;
- стропы не должны иметь узлов, петли должны свободно перемещаться;
- острые кромки груза должны быть защищены прокладками.

3.3. Перемещение груза:
- перед подъемом груза подать звуковой сигнал;
- приподнять груз на 200-300 мм, убедиться в правильности строповки и надежности тормозов;
- перемещать груз на высоте не менее 0,5 м над встречающимися на пути предметами;
- не допускать раскачивания груза;
- опускать груз плавно, без резких торможений;
- после укладки груза на место ослабить канат и дать команду стропальщику на отцепку стропов.

3.4. Управление краном производится:
- с помощью органов управления, расположенных на кране или пульте управления;
- в соответствии с сигналами стропальщика;
- при постоянном наблюдении за перемещаемым грузом и рабочей зоной.

3.5. Сигналы, подаваемые стропальщиком:
- «Поднять» - рука поднята вверх, ладонь обращена вперед;
- «Опустить» - рука опущена вниз, ладонь обращена вниз;
- «Влево», «Вправо» - горизонтальное движение руки в соответствующую сторону;
